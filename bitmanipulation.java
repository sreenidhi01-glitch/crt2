class bitmanipulation{
    public static void main(String[] args) {
        int a=5,b=2;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(~a);
        System.out.println(a^b);
        System.out.println(a<<2);
        System.out.println(b>>1);

    }
}